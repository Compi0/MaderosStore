    private void ofertasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ofertasActionPerformed
        int opc=0;
        String[] options = new String[] {"Crear Oferta", "Modificar Oferta", "\"Consultar Ofertas"};
        int response = JOptionPane.showOptionDialog(null, "Message", "Title",
        JOptionPane.DEFAULT_OPTION, JOptionPane.PLAIN_MESSAGE,
        null, options, options[0]);
     //   int j=0;
         if(response == 0){
             try{
            float ans;
            ans= Float.parseFloat( JOptionPane.showInputDialog("Ingresse el descuento: "));
      //  j=ans;
             admin.crearOfertas(9, ans);
             
             }catch(HeadlessException | NumberFormatException e){
                             JOptionPane.showMessageDialog(null, "ERROR", "ERROR"+e, JOptionPane.INFORMATION_MESSAGE);

             }
        } 
         if(response == 1){
             
             String[] p = new String[] {"Eliminar oferta", "Modificar Oferta"};
        int r = JOptionPane.showOptionDialog(null, "Message", "Title",
        JOptionPane.DEFAULT_OPTION, JOptionPane.PLAIN_MESSAGE,
        null, p, p[0]);
             if(r==0){//eliminar oferta
                 
                  opc= JOptionPane.showInternalConfirmDialog(null, "Esta seguro de eliminar esta oferta?", "Alerta",JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.INFORMATION_MESSAGE);
         //   System.out.println(opc);
           if(opc==1){//NO
               JOptionPane.showInternalMessageDialog(null, "Operacion cancelada",
             "Informacion", JOptionPane.INFORMATION_MESSAGE);
               new Menu_admin(con).setVisible(true);
           }if(opc==0){//SI
                JOptionPane.showInternalMessageDialog(null, "Oferta eliminada exitosamente!",
             "Informacion", JOptionPane.INFORMATION_MESSAGE);
              admin.modificarOfertas(1, 9);
                 new Menu_admin(con).setVisible(true);
           }
             
             }if(r==1){//modificar oferta
                  admin.modificarOfertas(2, 9);
             }
         }
         if(response == 2){
             new Ofertas(con).setVisible(true);
         }
    }//GEN-LAST:event_ofertasActionPerformed

